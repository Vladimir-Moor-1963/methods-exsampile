public class ArreySecondExample {
    public static void main(String[] args) {
        int[] arrey = {4, 7, -7, 22, 4};
        for (int i = 0; i < arrey.length; i++) {
            if (arrey[i] > 0){
                System.out.println(arrey[i]);
                System.out.println(i);
                System.out.println("--------------");
            }

        }
    }





}
